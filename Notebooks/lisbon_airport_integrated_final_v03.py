(
    name_code_pairs[name_code_pairs["name"].isin(flagged)]
    .assign(code_n=lambda x: x.groupby("name").cumcount() + 1)
    .pivot(index="name", columns="code_n", values="code")
    .rename(columns={1: "code_1", 2: "code_2"})
    .reset_index()
)

