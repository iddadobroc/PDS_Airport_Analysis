    'AT','BE','BG','HR','CY','CZ','DK','EE','FI','FR',
    'DE','EL','GR','HU','IS','IE','IT','LV','LI','LT','LU',
    'MT','NL','NO','PL','RO','SK','SI','ES','SE','CH',
    'GB','UK','AL','BA','ME','MK','RS','TR','UA','MD','BY'
