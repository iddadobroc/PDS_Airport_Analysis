print("=" * 55)

for df_m, col, label in metrics_growth:
    print(f"\n  {label}")
    print("-" * 45)
    for start, end, period_label in periods:
        rate = simple_growth(df_m, col, start, end)
        print(f"  {period_label}   {rate:+.1f}%")

